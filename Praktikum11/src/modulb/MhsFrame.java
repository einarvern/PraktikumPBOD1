    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
    int baris = tblMhs.getSelectedRow();
    if(baris >= 0){
        int idMhs = Integer.parseInt(tblMhs.getValueAt(baris, 0).toString());
        String namaBaru = txtNama.getText();
        servis.update(idMhs, namaBaru);
        txtNama.setText("");
        loadData();
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    int brs = tblMhs.getSelectedRow();
    if(brs >= 0){
        int idM = Integer.parseInt(tblMhs.getValueAt(brs, 0).toString());
        servis.delete(idM);
        loadData();
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

