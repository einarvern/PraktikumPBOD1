private void loadData() {
    modelTabel.setRowCount(0);
    List<Mahasiswa> data = servis.getAll();
    for (Mahasiswa mhs : data) {
        modelTabel.addRow(new Object[]{mhs.getId(), mhs.getNama()});
    }
}

